import torchvision
import torchvision.transforms as transforms
import os
import pickle
import numpy as np
from PIL import Image


class ImbalanceCIFAR10_MOCO(torchvision.datasets.CIFAR10):
    cls_num = 10

    def __init__(self, root, imb_type='exp', imb_factor=0.01, rand_number=0, train=True,
                 transform=None, target_transform=None, download=True, backup=True):
        super(ImbalanceCIFAR10_MOCO, self).__init__(root, train, transform, target_transform, download)
        np.random.seed(rand_number)
        img_num_list = self.get_img_num_per_cls(self.cls_num, imb_type, imb_factor)
        self.gen_imbalanced_data(img_num_list)
        self.cls_num_list = self.get_cls_num_list()
        self.data_back = self.data.copy() if backup else None
        self.targets_back = self.targets.copy() if backup else None

    def get_img_num_per_cls(self, cls_num, imb_type, imb_factor):
        img_max = len(self.data) / cls_num
        img_num_per_cls = []
        if imb_type == 'exp':
            for cls_idx in range(cls_num):
                num = img_max * (imb_factor**(cls_idx / (cls_num - 1.0)))
                img_num_per_cls.append(int(num))
        elif imb_type == 'step':
            for cls_idx in range(cls_num // 2):
                img_num_per_cls.append(int(img_max))
            for cls_idx in range(cls_num // 2):
                img_num_per_cls.append(int(img_max * imb_factor))
        else:
            img_num_per_cls.extend([int(img_max)] * cls_num)
        return img_num_per_cls

    def gen_imbalanced_data(self, img_num_per_cls):
        new_data = []
        new_targets = []
        targets_np = np.array(self.targets, dtype=np.int64)
        classes = np.unique(targets_np)
        # np.random.shuffle(classes)
        self.num_per_cls_dict = dict()
        for the_class, the_img_num in zip(classes, img_num_per_cls):
            self.num_per_cls_dict[the_class] = the_img_num
            idx = np.where(targets_np == the_class)[0]
            np.random.shuffle(idx)
            selec_idx = idx[:the_img_num]
            new_data.append(self.data[selec_idx, ...])
            new_targets.extend([the_class, ] * the_img_num)
        new_data = np.vstack(new_data)
        self.data = new_data
        self.targets = new_targets
        
    def get_cls_num_list(self):
        cls_num_list = []
        for i in range(self.cls_num):
            cls_num_list.append(self.num_per_cls_dict[i])
        return cls_num_list

    def set_cls_dataset(self, cls):
        if self.data_back is None:
            self.data_back = self.data.copy()
            self.targets_back = self.targets.copy()
        if cls == 0:
            start = 0
        else:
            start = sum(self.cls_num_list[:cls])
        end = self.cls_num_list[cls] + start
        self.data = self.data_back[start: end]
        self.targets = self.targets_back[start:end]

        print('num cls {} data: {}'.format(cls, len(self.data)))

    def reset_data(self):
        if self.data_back is not None:
            self.data = self.data_back.copy()
            self.targets = self.targets_back.copy()

    def __getitem__(self, index):
        """
        Args:
            index (int): Index
        Returns:
            tuple: (image, target) where target is index of the target class.
        """
        img, target = self.data[index], self.targets[index]

        # doing this so that it is consistent with all other datasets
        # to return a PIL Image
        img = Image.fromarray(img)

        if self.transform is not None:
            samples = [tr(img) for tr in self.transform]

        if self.target_transform is not None:
            target = self.target_transform(target)

        return samples, target, np.array(index)


